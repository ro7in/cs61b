public class SinglePixel {

    private short red;
    private short green;
    private short blue;

    public void setred (short red) {
        this.red = red;
    }

    public void setgreen (short green) {
        this.green = green;
    }

    public void setblue (short blue) {
        this.blue = blue;
    }

    public short getred() {
        return red;
    }

    public short getgreen() {
        return green;
    }

    public short getblue() {
        return blue;
    }
}
